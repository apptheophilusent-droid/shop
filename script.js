// Cart state
let cart = JSON.parse(localStorage.getItem("cart")) || [];

const productsContainer = document.getElementById("products");
const cartSection = document.getElementById("cart-section");
const cartItemsContainer = document.getElementById("cart-items");
const cartTotalEl = document.getElementById("cart-total");
const btnCheckout = document.getElementById("btn-checkout");
const orderSection = document.getElementById("order-section");
const orderForm = document.getElementById("order-form");
const toast = document.getElementById("toast");

// Toggle Cart Panel
document.getElementById("toggle-cart").addEventListener("click", () => {
  cartSection.classList.toggle("open");
});
document.getElementById("close-cart").addEventListener("click", () => {
  cartSection.classList.remove("open");
});

// Save cart to localStorage
function saveCartToStorage() {
  localStorage.setItem("cart", JSON.stringify(cart));
}

// Show toast
function showToast(message = "Added to cart ðŸ›ï¸") {
  toast.textContent = message;
  toast.classList.add("show");
  setTimeout(() => toast.classList.remove("show"), 3000);
}

// Render Products
function renderProducts() {
  productsContainer.innerHTML = "";
  products.forEach((product) => {
    const card = document.createElement("div");
    card.className = "product-card";
    card.innerHTML = `
      <img src="${product.image}" loading="lazy" alt="${product.name}" />
      <div class="product-info">
        <div>
          <h3 class="product-name">${product.name}</h3>
          <p class="product-price">â‚µ${product.price.toFixed(2)}</p>
        </div>
        <button class="btn-add" data-id="${product.id}">Add to Cart</button>
      </div>
    `;
    productsContainer.appendChild(card);
  });
}

// Render Cart
function renderCart() {
  if (cart.length === 0) {
    cartItemsContainer.innerHTML = `
      <p>Your cart is empty.</p>
      <button onclick="cartSection.classList.remove('open')" class="btn-clear">Browse Products</button>
    `;
    cartTotalEl.textContent = "0.00";
    btnCheckout.disabled = true;
    return;
  }

  btnCheckout.disabled = false;
  cartItemsContainer.innerHTML = "";

  cart.forEach((item) => {
    const product = products.find((p) => p.id === item.id);
    const div = document.createElement("div");
    div.className = "cart-item";
    div.innerHTML = `
      <div class="cart-item-name">${product.name}</div>
      <div class="cart-item-qty">
        <button class="btn-decrease" data-id="${item.id}">âˆ’</button>
        <span>${item.qty}</span>
        <button class="btn-increase" data-id="${item.id}">+</button>
      </div>
      <div class="cart-item-price">â‚µ${(product.price * item.qty).toFixed(2)}</div>
    `;
    cartItemsContainer.appendChild(div);
  });

  const total = cart.reduce((sum, item) => {
    const p = products.find((prod) => prod.id === item.id);
    return sum + p.price * item.qty;
  }, 0);

  cartTotalEl.textContent = total.toFixed(2);
}

// Add to Cart
productsContainer.addEventListener("click", (e) => {
  if (!e.target.classList.contains("btn-add")) return;
  const id = Number(e.target.dataset.id);
  const item = cart.find((i) => i.id === id);

  if (item) {
    item.qty++;
  } else {
    cart.push({ id, qty: 1 });
  }

  saveCartToStorage();
  renderCart();
  showToast();
});

// Cart Quantity Buttons
cartItemsContainer.addEventListener("click", (e) => {
  const id = Number(e.target.dataset.id);
  const item = cart.find((i) => i.id === id);

  if (e.target.classList.contains("btn-increase")) {
    item.qty++;
  } else if (e.target.classList.contains("btn-decrease")) {
    item.qty--;
    if (item.qty <= 0) {
      cart = cart.filter((i) => i.id !== id);
    }
  }

  saveCartToStorage();
  renderCart();
});

// Checkout button
btnCheckout.addEventListener("click", () => {
  cartSection.classList.remove("open");
  orderSection.style.display = "block";
  window.scrollTo({ top: orderSection.offsetTop, behavior: "smooth" });
});

// Submit order
orderForm.addEventListener("submit", function (e) {
  e.preventDefault();

  const name = document.getElementById("name").value.trim();
  const phone = document.getElementById("phone").value.trim();
  const address = document.getElementById("address").value.trim();

  if (!name || !phone || !address) {
    alert("Please fill in all fields.");
    return;
  }

  const totalAmount = cart.reduce((sum, item) => {
    const product = products.find((p) => p.id === item.id);
    return sum + product.price * item.qty;
  }, 0);

  FlutterwaveCheckout({
    public_key: "FLWPUBK_TEST-xxxxxxxxxxxxxxxxxxxxx-X",
    tx_ref: "mybrand-" + Date.now(),
    amount: totalAmount,
    currency: "USD",
    customer: { email: "customer@example.com", phonenumber: phone, name },
    callback: (data) => {
      alert("Payment complete! Transaction ID: " + data.transaction_id);
      cart = [];
      saveCartToStorage();
      renderCart();
      orderForm.reset();
      orderSection.style.display = "none";
    },
    onclose: () => {
      alert("Payment was cancelled");
    },
    customizations: {
      title: "MyBrand Fashion Store",
      description: "Order Payment",
      logo: "https://via.placeholder.com/100x50?text=MyBrand",
    },
  });
});

